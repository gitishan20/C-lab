#include<stdio.h>
/*Area and Perimeter of rectangles */
int main()
{
    int l_84,b_84,area_84,perimeter_84;
    printf("Enter length and breadth of rectangles:\n ");
    scanf("%d%d",&l_84,&b_84);
    area_84 =l_84 * b_84;
    perimeter_84 =2*(l_84 + b_84);
    printf("Area of the rectangle is %d \n",area_84);
    printf("Perimeter of the rectangle is %d \n",perimeter_84);

    return 0;
}
  
  
  
  
  
  
  
  
  
  
  
  
   